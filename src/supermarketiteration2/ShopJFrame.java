    public void populateProductTextFields(Product product) {
        selectedProduct = product;

        jSupplierSelectionBox.setSelectedItem(selectedProduct.getSupplier());
        jDeliveryCompanySelectionBox.setSelectedItem(selectedProduct.getDeliveryCompany());
        jProductNameTextField.setText(selectedProduct.getName());
        jCategorySelectionBox.setSelectedItem(selectedProduct.getCategory());
        jPriceTextField.setText(String.valueOf(selectedProduct.getPrice()));
        jUnitCostTextField.setText(String.valueOf(selectedProduct.getUnitCost()));
        jQuantityTextField.setText(String.valueOf(selectedProduct.getQuantity()));
        jWeightTextField.setText(String.valueOf(selectedProduct.getWeight()));
        jMinStockTextField.setText(String.valueOf(selectedProduct.getMinimumStockLevel()));
        jMaxStockTextField.setText(String.valueOf(selectedProduct.getMaximumStockLevel()));
        jImageNameTextField.setText(selectedProduct.getImageName());
        jDateOfLastOrderPlacementTextField.setText(selectedProduct.getSimpledateformat().format(selectedProduct.getDateOfLastOrderPlacement()));
        jWeightCategorySelectionBox.setSelectedItem(selectedProduct.getWeightCategory());

        switch (selectedProduct.getCategory()) {
            case "Food":
                FoodProduct fProduct = (FoodProduct) selectedProduct;
                jShelfLifeTextField.setText(String.valueOf(fProduct.getShelfLife()));
                break;
            case "Book":
                BookProduct bProduct = (BookProduct) selectedProduct;
                jISBNTextField.setText(bProduct.getISBN());
                jAuthorTextField.setText(bProduct.getAuthor());
                break;
            case "Kitchen":
                KitchenProduct kProduct = (KitchenProduct) selectedProduct;
                jGuarunteeTimeTextField.setText(String.valueOf(kProduct.getGuarunteeDuration()));
                break;
            case "Electronic":
                ElectronicProduct eProduct = (ElectronicProduct) selectedProduct;
                jGuarunteeTimeTextField.setText(String.valueOf(eProduct.getGuarunteeDuration()));
                jVoltageTextField.setText(String.valueOf(eProduct.getVoltage()));
                break;

        }

    }

