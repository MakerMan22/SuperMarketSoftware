    private void jAddToBasketButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddToBasketButtonActionPerformed
        Product product = theProductList.find(jCustomerProductSelectionBox.getSelectedItem().toString());
        Integer quantity = Integer.valueOf(jCustomerQuanityTextField.getText());

        if (product.getQuantity() < quantity) {
            this.displayWarning("Sorry there is not enough in stock, please lower quantity");
        } else {

            try {//JAVA IS PASS BY VALUE SO A CLONE OF THE ORIGINAL MUST BE MADE BEFORE ADDING TO BASKET
                //IF NOT THEN ANY MODIFICATION WILL MODIFY PRODUCT IN BOTH LISTS.

                switch (product.getCategory()) {
                    case "Food":
                        FoodProduct fProduct = (FoodProduct) product.clone();
                        fProduct.setQuantity(quantity);
                        castedCustomer.getTheBasket().add(fProduct);
                        break;
                    case "Book":
                        BookProduct bProduct = (BookProduct) product.clone();
                        bProduct.setQuantity(quantity);
                        castedCustomer.getTheBasket().add(bProduct);
                        break;
                    case "Kitchen":
                        KitchenProduct kProduct = (KitchenProduct) product.clone();
                        kProduct.setQuantity(quantity);
                        castedCustomer.getTheBasket().add(kProduct);
                        break;
                    case "Electronic":
                        ElectronicProduct eProduct = (ElectronicProduct) product.clone();
                        eProduct.setQuantity(quantity);
                        castedCustomer.getTheBasket().add(eProduct);

                }
                castedCustomer.getTheBasket().saveToFile(true, castedCustomer.getName() + " " + castedCustomer.getRole() + " Basket");
                castedCustomer.getTheBasket().displayTotalOrderCost(jTotalOrderCostTextField);
                this.updateComboBox("Basket");
                castedCustomer.getTheBasket().displayTotalOrderCost(jTotalOrderCostTextField);
                this.displayWarning("Product added");

            } catch (IOException ex) {
                Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (CloneNotSupportedException ex) {
                Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jAddToBasketButtonActionPerformed

