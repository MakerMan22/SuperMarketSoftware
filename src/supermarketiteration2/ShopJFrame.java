    public void showCustomersDetails() throws IOException, FileNotFoundException, ParseException {

        String title = jMainTabbedPane.getTitleAt(jMainTabbedPane.getSelectedIndex());//PROCESS IS ONLY ACTIVATED WHEN ON CORRECT TAB
        if (title.equalsIgnoreCase("Customer Database")) {

            jEmployeeViewCustomerBasketTextArea.setText("");
            jEmployeeViewCustomerWishListTextArea.setText("");

            User currentCustomer = theUserList.find(jCustomerList.getSelectedValue());
            Customer castedCustomer = (Customer) currentCustomer;

            if (castedCustomer.getTheBasket().checkIfFileExists(castedCustomer.getName() + " " + castedCustomer.getRole() + " Basket.txt")) {
                castedCustomer.getTheBasket().Products.clear();
                castedCustomer.getTheBasket().loadFromFile(castedCustomer.getName() + " " + castedCustomer.getRole() + " Basket");
                castedCustomer.getTheBasket().display(jEmployeeViewCustomerBasketTextArea);
            }
            if (castedCustomer.getTheBasket().checkIfFileExists(castedCustomer.getName() + " " + castedCustomer.getRole() + " WishList.txt")) {
                castedCustomer.getTheWishList().Products.clear();
                castedCustomer.getTheWishList().loadFromFile(castedCustomer.getName() + " " + castedCustomer.getRole() + " WishList");
                castedCustomer.getTheWishList().display(jEmployeeViewCustomerWishListTextArea);
            }

        }
    }

