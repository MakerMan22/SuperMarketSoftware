    private void jPlaceOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPlaceOrderButtonActionPerformed

        try {
//CONNECTS TO SERVER
            aClient = new SuperMarketClient();
//SENDS DATA TO SERVER 
            aClient.sendToServer(jStaffSortCodeTextField.getText(), jStaffAccountNumberTextField.getText(), jCostTextField.getText());
//DISPLAYS RESPONSE FROM SERVER
            String message = aClient.readInputData();
            this.displayWarning(message);
//GOES THROUGH ALL PRODUCTS IN CUSTOMERS BASKET AND UPDATES QUANTITY OF STOCK IN MAIN STOCK LIST ACCORDINGLY
            if (message.equalsIgnoreCase("Transaction Approved")) {

//SETS THE NEW QUANTITY                          
                selectedProduct.setQuantity(selectedProduct.getQuantity() + Integer.valueOf(jSuggestedQuantityTextField.getText()));
                theProductList.updateLastOrderDate(selectedProduct);
                theProductList.saveToFile(false, theProductList.getFilename());

                selectedProduct.setDateOfLastOrderPlacement(new Date());

                jDateOfLastOrderTextField.setText(selectedProduct.dateToString(selectedProduct.getDateOfLastOrderPlacement()));
                jSingleProductDetailsTextArea.setText("");
                jOrderETATextField.setText(theDeliveryCompanyList.calculateETA(aDeliveryCompany.getDeliveryTime()));
                theProductList.displayReorders(jProductReorderTextArea);

                jSuggestedQuantityTextField.setText("");
                jReOrderCategoryTextField.setText("");
                jDateOfLastOrderTextField.setText("");
                jAdvisedOrderDateTextField.setText("");
                jCostTextField.setText("");

            }

        } catch (ParseException ex) {
            Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jPlaceOrderButtonActionPerformed

