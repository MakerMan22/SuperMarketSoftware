    private void jLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginButtonActionPerformed
//if the shop owner or employee logs in, then the tabs will change accordingly,
//if the customer logs in then his details are retrieved and allocated to customer object for duration of shopping time. 

//Attributes initialised here to allow for easy reading below
        String name = jNameTextField.getText();
        String password = jPasswordField.getText();
        String role = jRoleSelectionBox.getSelectedItem().toString();

        if (theUserList.logIn(name, password, role) != null) {

            switch (role) {

                case "Customer":
                    //This resturns a user object then downcasts it to a customer object to access customer methods. 
                    currentCustomer = theUserList.logIn(name, password, role);
                    castedCustomer = (Customer) currentCustomer;
                    castedCustomer.getTheWishList().Products.clear();
                    castedCustomer.getTheBasket().Products.clear();
                    jBrowseBasketTextArea.setText("");  
                    jBrowseWishListTextArea.setText("");
                    this.updateComboBox("WishList");
                    this.updateComboBox("Basket");
                     {
                        try {
                            if (castedCustomer.getTheBasket().checkIfFileExists(castedCustomer.getName() + " " + castedCustomer.getRole() + " Basket.txt")) {                         
                                castedCustomer.getTheBasket().loadFromFile(castedCustomer.getName() + " " + castedCustomer.getRole() + " Basket");                               
                            }
                            if (castedCustomer.getTheWishList().checkIfFileExists(castedCustomer.getName() + " " + castedCustomer.getRole() + " WishList.txt")) {                                
                                castedCustomer.getTheWishList().loadFromFile(castedCustomer.getName() + " " + castedCustomer.getRole() + " WishList");                            
                            }
                        } catch (IOException ex) {
                            Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (ParseException ex) {
                            Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                     
                     
                     
                    this.showTabs("Customer");
                    jTotalOrderCostTextField.setText("");
                    jSortCodeTextField.setText("");
                    jAccountNumberTextField.setText("");  
                    castedCustomer.getTheBasket().display(jBrowseBasketTextArea);
                    castedCustomer.getTheWishList().display(jBrowseWishListTextArea);
                    this.updateComboBox("WishList");
                    this.updateComboBox("Basket");
                    castedCustomer.getTheBasket().displayTotalOrderCost(jTotalOrderCostTextField);
                     
                    break;

                case "Employee":
                    this.showTabs("Employee");
                    this.populateListModel();
                    break;

                case "Shop Owner":
                    this.showTabs("Shop Owner");
                    this.populateListModel();
                    break;
            }
        } else {
            this.displayWarning("Cannot login, please check login and password");
        }

    }//GEN-LAST:event_jLoginButtonActionPerformed

