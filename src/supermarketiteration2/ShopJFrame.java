    private void jPurchaseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPurchaseButtonActionPerformed
        try {
//CONNECTS TO SERVER
            aClient = new SuperMarketClient();
//SENDS DATA TO SERVER 
            aClient.sendToServer(jSortCodeTextField.getText(), jAccountNumberTextField.getText(), jTotalOrderCostTextField.getText());
//DISPLAYS RESPONSE FROM SERVER (HANGS UNTIL SERVER RESPONDS) 
            String message = aClient.readInputData();
            this.displayWarning(message);
//GOES THROUGH ALL PRODUCTS IN CUSTOMERS BASKET AND SUBTRACTS QUANTITY OF STOCK IN MAIN STOCK LIST ACCORDINGLY
            if (message.equalsIgnoreCase("Transaction Approved")) {
                for (Product inputProduct : castedCustomer.getTheBasket().Products) {
                    Product recipientProduct = theProductList.find(inputProduct.getName());
                    theProductList.updateQuantity(inputProduct, recipientProduct);
                }
                theProductList.saveToFile(false, theProductList.getFilename());
//EMPTIES BASKET && UPDATES TEXT AREAS
                castedCustomer.getTheBasket().Products.clear();
                castedCustomer.getTheBasket().display(jBrowseBasketTextArea);
                castedCustomer.getTheBasket().saveToFile(false, castedCustomer.getTheBasket().getFilename());

                theProductList.display(jBrowseProductsTextArea);
                selectedProduct.display(false, jCustomerSingleProductDetailsTextArea);
                jTotalOrderCostTextField.setText("");
                jSortCodeTextField.setText("");
                jAccountNumberTextField.setText("");
            }

        } catch (IOException ex) {
            Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(ShopJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jPurchaseButtonActionPerformed

